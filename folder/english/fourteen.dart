enum Day {
  Monday,
  Tuesday,
  Wednesday,
  Thursday,
  Friday,
  Saturday,
  Sunday,
}

main() {
  Day day =Day.Friday;

  switch(day){
    case Day.Monday:
    print("monday");
    break;
    case Day.Tuesday:
    print("tuesday");
    break;
    case Day.Wednesday:
    print("wednesday");
    break;
    case Day.Thursday:
    print("thursday");
    break;
    case Day.Friday:
    print("friday");
    break;
    case Day.Saturday:
    print("saturday");
    break;
    case Day.Sunday:
    print("sunday");
        
  }

}
